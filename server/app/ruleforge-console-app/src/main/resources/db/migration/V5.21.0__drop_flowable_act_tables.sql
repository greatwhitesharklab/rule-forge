-- V5.21+: 自建 FlowEngine(V5.20 commit 4984671)完全替换 Flowable 8 引擎。
-- 决策流执行不再读写 flowable_db,act_*/flw_* 表清掉,免得 schema 漂移。
--
-- [data-loss] 不可逆 — 任何 act_*/flw_* 上的历史 runtime / history 数据都会丢。
-- 运维前置:合入生产前 mysqldump flowable_db > /tmp/flowable_db_$(date +%s).sql
--
-- 表格清单来源:
--   V1__flowable_engine_mysql.sql         (32 张 ACT_* 表,Flowable 8.0.0 engine)
--   V2__flowable_idm_version.sql          (1  张 ACT_ID_PROPERTY)
--   V3__flowable_eventregistry.sql        (4  张 FLW_* 事件注册表)
--   V4__flowable_variable_scope_columns.sql (只改列,无新表 — 不需 DROP)
--
-- 用 DROP TABLE IF EXISTS 幂等,Flyway 重跑不会报 "table not exist"。

