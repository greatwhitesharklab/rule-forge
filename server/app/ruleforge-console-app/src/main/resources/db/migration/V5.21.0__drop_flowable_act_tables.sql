-- 清理 Flyway schema_history 里 V1..V4 flowable 记录的 checksum
-- (V5.x 老 evaluate 走自建 FlowEngine 之后,这些记录的 engine 类已不在 classpath)
DELETE FROM flyway_schema_history
 WHERE version IN ('V1', 'V2', 'V3', 'V4')
   AND description LIKE 'flowable%';
